# While Looping: while the statement is True, the code inside the block will be executed. 
# So, this code will print the number from 1 to 10.
# The while loop needs a “loop condition.” If it stays True, it continues iterating. 
# In this example, when num is 11 the loop condition equals False.

num = 1

while (num <= 10):
    print(num)
    num += 1

for i in range(1, 11):
  print(i)